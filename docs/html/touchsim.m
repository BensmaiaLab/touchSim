%% TouchSim

%% Background
%
% TouchSim is Matlab toolbox for simulating the responses of populations of
% tactile afferents terminating all over the glabrous surface of the hand.
%
% The model is described in detail in the following paper:
%
% * Saal HP, Delhaye BP, Rayhaun BC, Bensmaia SJ (2017). Simulating tactile
% signals from the whole hand with millisecond precision. _Proc Natl Acad
% Sci U S A_, <http://doi.org/10.1073/pnas.1704856114 doi:10.1073/pnas.1704856114>

%% Requirements
%
% TouchSim will run on Matlab 2015b or newer. It requires the following
% toolboxes:
%
% * Signal Processing Toolbox
% * Image Processing Toolbox
% * Statistics and Machine Learning Toolbox
% * Curve Fitting Toolbox
% * Bioinformatics Toolbox

%% Installation
%
% The toolbox does not require installation. Run |setup_path| from the root
% directory to add all relevant folders to the Matlab path. This
% documentation is then accessible through Matlab's help browser by typing
% |doc|.

%% Further documentation
% * <classes.html Classes>: Describes the object classes defined by
% TouchSim.
% * <funs.html Generator functions>: Describes functions for quickly generating
% commonly used afferent populations and stimuli.
% * <util.html Other functions>: Useful functions for using TouchSim and
% plotting results.
% * <gui.html GUI>: Shows how to construct afferent populations using the
% GUI.
% * <examples.html Examples>: Gives practical examples on working with
